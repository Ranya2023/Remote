// Captures an off-screen-rendered report node (see the -9999px positioning
// pattern used by callers) and produces a PNG download.
export async function exportReportPNG(node: HTMLElement, filename: string) {
  const canvas = await html2canvas(node, { backgroundColor: '#0b1020', scale: 2, useCORS: true });
  const blob: Blob | null = await new Promise((resolve) => canvas.toBlob(resolve, 'image/png'));
  if (!blob) return;
  const url = URL.createObjectURL(blob);
  const a = document.createElement('a');
  a.href = url; a.download = filename; a.click();
  URL.revokeObjectURL(url);
}

// Same capture, sliced across as many A4 pages as needed and dropped into a PDF.
export async function exportReportPDF(node: HTMLElement, filename: string) {
  const canvas = await html2canvas(node, { backgroundColor: '#0b1020', scale: 2, useCORS: true });
  const pdf = new jsPDF({ unit: 'pt', format: 'a4' });
  const pageWidth = pdf.internal.pageSize.getWidth();
  const pageHeight = pdf.internal.pageSize.getHeight();
  const imgWidth = pageWidth;
  const imgHeight = (canvas.height * imgWidth) / canvas.width;

  if (imgHeight <= pageHeight) {
    pdf.addImage(canvas.toDataURL('image/png'), 'PNG', 0, 0, imgWidth, imgHeight);
  } else {
    // Slice the tall canvas into page-height chunks so long leaderboards /
    // question lists flow onto additional pages instead of getting cut off.
    const pxPerPage = Math.floor((pageHeight * canvas.width) / imgWidth);
    let renderedPx = 0;
    let first = true;
    while (renderedPx < canvas.height) {
      const sliceHeight = Math.min(pxPerPage, canvas.height - renderedPx);
      const pageCanvas = document.createElement('canvas');
      pageCanvas.width = canvas.width;
      pageCanvas.height = sliceHeight;
      const ctx = pageCanvas.getContext('2d');
      if (ctx) {
        ctx.fillStyle = '#0b1020';
        ctx.fillRect(0, 0, pageCanvas.width, pageCanvas.height);
        ctx.drawImage(canvas, 0, renderedPx, canvas.width, sliceHeight, 0, 0, canvas.width, sliceHeight);
      }
      if (!first) pdf.addPage();
      pdf.addImage(pageCanvas.toDataURL('image/png'), 'PNG', 0, 0, imgWidth, (sliceHeight * imgWidth) / canvas.width);
      renderedPx += sliceHeight;
      first = false;
    }
  }
  pdf.save(filename);
}
