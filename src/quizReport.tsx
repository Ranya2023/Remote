import html2canvas from 'html2canvas';
import jsPDF from 'jspdf';

// Shared quiz-results report - used by both Present.tsx (teacher) and
// AudienceJoin.tsx (students) so everyone downloads the exact same styled
// report. Renders as real (off-screen) HTML, then html2canvas rasterizes
// it - one capture feeds both the PNG and the PDF exports, so they always
// match pixel-for-pixel.

export interface ReportQuestion {
  id: string;
  question: string;
  correctText: string;
  source?: string;
  correctCount: number;
  incorrectCount: number;
}
export interface ReportParticipant {
  rank: number;
  name: string;
  emoji: string;
  score: number;
  correctCount: number;
  totalQuestions: number;
}
export interface QuizReportData {
  title: string;
  dateLabel: string;
  leaderboard: ReportParticipant[];
  questions: ReportQuestion[];
}

